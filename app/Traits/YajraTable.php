     * Datatable mockup for teacher resource
     *
     * @param mixed $collection
     *
     * @return JsonResponse
     * @throws Exception
     */

    public function TeacherMockup(mixed $collection): JsonResponse
    {
        return DataTables::of($collection)
            ->addIndexColumn()
            ->addColumn('action', function ($data) {
                return view('dashboard.admin.pages.teacher.datatables', compact('data'));
            })
            ->rawColumns(['action'])
            ->toJson();
    }

